    private void btn_GuardarClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_GuardarClienteMouseClicked
        // TODO add your handling code here:
        if (!"".equals(txt_DNICliente.getText()) && !"".equals(txt_NombreCliente.getText()) && !"".equals(txt_TelefonoCliente.getText()) && !"".equals(txt_DireccionCliente.getText())) {
            cliente.setDNI(Integer.parseInt(txt_DNICliente.getText()));
            cliente.setNombre(txt_NombreCliente.getText());
            cliente.setTelefono(Integer.parseInt(txt_TelefonoCliente.getText()));
            cliente.setDireccion(txt_DireccionCliente.getText());
            cliente.setRazon(txt_RazonSocialCliente.getText());
            cliente1.RegistrarCliente(cliente);
            LimpiarTable();
            LimpiarCliente();
            ListarCliente();
            JOptionPane.showMessageDialog(null, "Cliente Registrado");
        } else {
            JOptionPane.showMessageDialog(null, "Rellene todos los campos");
        }
    }//GEN-LAST:event_btn_GuardarClienteMouseClicked

    private void btn_EditarClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_EditarClienteMouseClicked
        // TODO add your handling code here:
        if ("".equals(txt_IDCliente.getText())) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        } else {

            if (!"".equals(txt_DNICliente.getText()) && !"".equals(txt_NombreCliente.getText()) && !"".equals(txt_TelefonoCliente.getText()) && !"".equals(txt_DireccionCliente.getText())) {
                cliente.setDNI(Integer.parseInt(txt_DNICliente.getText()));
                cliente.setNombre(txt_NombreCliente.getText());
                cliente.setTelefono(Integer.parseInt(txt_TelefonoCliente.getText()));
                cliente.setDireccion(txt_DireccionCliente.getText());
                cliente.setRazon(txt_RazonSocialCliente.getText());
                cliente.setID(Integer.parseInt(txt_IDCliente.getText()));
                cliente1.ModificarCliente(cliente);
                LimpiarTable();
                LimpiarCliente();
                ListarCliente();
                JOptionPane.showMessageDialog(null, "Cliente Modificado");
            } else {
                JOptionPane.showMessageDialog(null, "Rellene todos los campos");
            }
        }
    }//GEN-LAST:event_btn_EditarClienteMouseClicked

    private void btn_EliminarClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_EliminarClienteMouseClicked
        // TODO add your handling code here:
        if (!"".equals(txt_IDCliente.getText())) {
            int pregunta = JOptionPane.showConfirmDialog(null, "Esta seguro de Eliminar");

            if (pregunta == 0) {
                int ID = Integer.parseInt(txt_IDCliente.getText());
                cliente1.EliminarCliente(ID);
                LimpiarTable();
                LimpiarCliente();
                ListarCliente();
            } else {
                LimpiarCliente();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }
    }//GEN-LAST:event_btn_EliminarClienteMouseClicked

